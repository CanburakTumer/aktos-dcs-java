package com.aktos_elektronik.params;

public class Params {
	private static String host, pubPort, subPort;

	public static String getHost() {
		return host;
	}

	public static void setHost(String h) {
		host = h;
	}

	public static String getPubPort() {
		return pubPort;
	}

	public static void setPubPort(String pp) {
		pubPort = pp;
	}

	public static String getSubPort() {
		return subPort;
	}

	public static void setSubPort(String sp) {
		subPort = sp;
	}
	
	

}
